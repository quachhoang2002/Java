    private void createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createActionPerformed
        // TODO add your handling code here:
        try {
            // Get input data from text fields
            int productId = Integer.parseInt(product_id.getText().trim());
            int quantity = Integer.parseInt(quantity_txt.getText().trim());
            double totalPrice = Double.parseDouble(total_price_txt.getText().trim());
            int manufactureId = Integer.parseInt(manu_txt.getText().trim());

            // Create an ImportDTO object with the input data
            ImportDTO importDTO = new ImportDTO(productId, quantity, totalPrice, manufactureId);

            // Call the createImportOrder method from ImportBUS to handle the import process
            ImportBUS importBUS = new ImportBUS();
            boolean importSuccess = importBUS.createImportOrder(importDTO);

            // Check if the import was successful
            if (importSuccess) {
                // If import was successful, display a success message or update the UI as needed
                JOptionPane.showMessageDialog(this, "Import successful", "Success", JOptionPane.INFORMATION_MESSAGE);

                // Optionally, you can update the table or any other UI components to reflect the changes
                loadData(""); // Update the import table
            } else {
                // If import failed, display an error message or handle the failure as needed
                JOptionPane.showMessageDialog(this, "Import failed", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            // If there is a number format exception (e.g., invalid input), display an error message
            JOptionPane.showMessageDialog(this, "Invalid input. Please enter valid numbers.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_createActionPerformed

