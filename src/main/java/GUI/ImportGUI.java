    private void setupTableSelectionListener() {
        import_table.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent event) {
                if (!event.getValueIsAdjusting()) { // Prevent double events
                    displaySelectedEmployeeData();
                }
            }
        });
    }

    private void displaySelectedEmployeeData() {
        int selectedRow = import_table.getSelectedRow();
        if (selectedRow != -1) {
            int customerId = (Integer) import_table.getModel().getValueAt(selectedRow, 0);
            String name = (String) import_table.getModel().getValueAt(selectedRow, 1);
            String lastName = (String) import_table.getModel().getValueAt(selectedRow, 2);
            String phone = (String) import_table.getModel().getValueAt(selectedRow, 3);

            manu_txt.setText(name);
            product_id.setText(lastName);
            quantity_txt.setText(phone);
        } else {
            manu_txt.setText("");
            product_id.setText("");
            quantity_txt.setText("");
        }
    }

