    private void initUI() {
        // Initialize table model
        if (import_table == null) {
            // Initialize table model with column names
            tableModel = new DefaultTableModel(new Object[]{"ID", "Product Name", "Manufacture Name", "Quantity", "Total Price",}, 0);
            import_table = new JTable(tableModel);
        } else {
            // Update the existing table model
            tableModel = (DefaultTableModel) import_table.getModel();
        }
        import_table.setEnabled(false);
        loadData("");
    }

    // Method to update the table model with ImportDTO data
    public void updateTable(List<ImportDTO> imports) {
        tableModel.setRowCount(0); // Clear existing data

        // Populate the table model
        for (ImportDTO importDTO : imports) {
            Object[] row = new Object[]{
                importDTO.getId(),
                importDTO.getProdname(), // Display product name instead of product ID
                importDTO.getManuName(),
                importDTO.getQuantity(),
                importDTO.getTotalPrice(), // Display manufacture name instead of manufacture ID
                importDTO.getTime(),
            };
            tableModel.addRow(row);
        }
    }

    private void loadData(String productName) {
        ImportDAO dao = new ImportDAO();
        List<ImportDTO> imports = dao.findImportsByProductName(productName);
        updateTable(imports);
    }

